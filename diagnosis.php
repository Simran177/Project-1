    <h1 id="respiratory">Respiratory viral diseases</h1>
    <p>Respiratory viral diseases are contagious and commonly affect the upper or lower parts of your respiratory tract.
        <br> Common symptoms of a respiratory viral disease include:</p>
        <ul>
            <li>runny or stuffy nose</li>
            <li>coughing or sneezing</li>
            <li>fever</li>
            <li>body aches</li>
        </ul>
    

    <h3>Examples</h3>
    <p>Examples of respiratory diseases include:</p>
    <ul>
        <li><a href="https://www.healthline.com/health/cold-flu/flu">flu</a> </li>
        <li> <a href="https://www.healthline.com/health/cold-flu/cold"> common cold</a></li>
        <li><a href="https://www.healthline.com/health/viral-diseases#respiratory">respiratory syncytial virus infection</a> </li>
        <li>adenovirus infection</li>
        <li><a href="https://www.healthline.com/health/cold-flu/parainfluenza">parainfluenza virus infection</a> </li>
        <li><a href="https://www.healthline.com/health/severe-acute-respiratory-syndrome-sars">severe acute respiratory syndrome (SARS)</a></li>
    </ul>

    <h3>Transmission</h3>
    <p>Respiratory viruses are spread by droplets generated through coughing or sneezing. If someone with a viral illness coughs or sneezes nearby and you inhale these droplets, you may develop the disease.
        These viruses can also be spread through contaminated objects, such as doorknobs, tabletops, and personal items. If you touch one of these objects and then touch your nose or eyes, you could develop a disease.
    </p>
    
    <h3>Treatment</h3>
    <p>Respiratory viral diseases usually heal on their own. But over-the-counter (OTC) medications, including nasal decongestants, cough suppressants, and pain relievers, can help to reduce symptoms.
        In addition, Tamiflu, an antiviral drug, is sometimes prescribed if someone is in the very early stages of developing the flu.
    </p>

    <h3>Prevention</h3>
    <p>The best way to avoid respiratory viral diseases is to practice good personal hygiene. Wash your hands often, cover your mouth when you cough or sneeze, and limit your interactions with people who show symptoms of a respiratory condition.
        There’s also a <a href="https://www.healthline.com/health/flu-shot-pros-and-cons"> vaccine</a> that can help to reduce your risk of getting the seasonal flu.</p>
    
