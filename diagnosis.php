    <h1 id="extanthe">Exanthematous viral disease</h1>
    <p>Exanthematous viruses cause skin rashes. Many of them cause additional symptoms as well.
        Many of the viruses in this category, such as the measles virus, are highly contagious.
    </p>
    <h3>Examples</h3>
    <p>Examples of exanthematous viral diseases include:</p>
    <ul>
        <li><a href="https://www.healthline.com/health/measles">measles</a> </li>
        <li><a href="https://www.healthline.com/health/rubella">rubella</a> </li>
        <li>chickenpox/<a href="https://www.healthline.com/health/shingles-pictures">shingles</a></li>
        <li><a href="https://www.healthline.com/health/roseola"> roseola</a></li>
        <li><a href="https://www.healthline.com/health/smallpox">smallpox</a> </li>
        <li><a href="https://www.healthline.com/health/fifth-disease">fifth disease</a></li>
        <li>chikungunya virus infection</li>
    </ul>
    <h3>Transmission</h3>
    <p>Many exanthematous viruses are spread through respiratory droplets from the cough or sneeze of someone with the virus.
        <br>
        Other exanthematous viral diseases, such as chickenpox and smallpox, can be transmitted by coming into contact with fluid in broken skin lesions.
        <br>
        Shingles only occurs in people who’ve had chickenpox at some point. It’s a reactivation of the varicella-zoster virus that’s been lying dormant in your cells.</p>
        <br>
        Chikungunya virus is spread through a mosquito bite and cannot be transmitted from person to person.

    <h3>Treatment</h3>
    <p>Treating exanthematous viral diseases focuses on managing symptoms. Fever-reducing medications, such as acetaminophen, can help with some of the more bothersome symptoms.
        <br>
        Antiviral drugs, such as acyclovir, may be given for chickenpox or shingles.</p>
    
    <h3>Prevention</h3>
    <p>Measles, rubella, chickenpox, shingles, and smallpox can all be prevented through vaccination. You can reduce your risk of a chikungunya virus infection by protecting yourself from mosquito bites.
        <br><a href="https://www.healthline.com/health/rashes-with-viruses">Learn more about viral rashes.</a></p>
    

