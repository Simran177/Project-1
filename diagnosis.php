    <h1 id="neuro">Neurologic viral diseases</h1>
    <p>Some viruses can infect the brain and surrounding tissues, causing neurologic viral diseases. This can result in a range of symptoms, including:
    </p>
    <ul>
        <li>fever</li>
        <li>confusion</li>
        <li>drowsiness</li>
        <li>seizures</li>
        <li>coordination problems</li>
    </ul>

    <h3>Examples</h3>
    <p>Examples of neurologic viral diseases include:  </p>
    <ul>
        <li><a href="https://www.healthline.com/health/poliomyelitis"> polio</a></li>
        <li>viral <a href="https://www.healthline.com/health/meningitis">meningitis</a> </li>
        <li>viral <a href="https://www.healthline.com/health/encephalitis"> encephalitis</a></li>
        <li><a href="https://www.healthline.com/health/rabies">rabies</a> </li>
    </ul>

    <h3>Transmission</h3>
    <p>Many neurologic viruses are spread through the bite of an infected animal or bug, such as a mosquito or tick.
        <br>Other viruses, such poliovirus and other enteroviruses, are quite contagious and spread through close contact with someone with the virus. Contaminated objects can also contribute to the spread of these viruses.
        </p>

    <h3>Treatment</h3>
    <p>There’s no specific treatment for people with mild viral meningitis or encephalitis. Getting plenty of rest, staying hydrated, and taking <a href="https://www.healthline.com/health/pain-relief/otc-anti-inflammatories">OTC anti-inflammatories</a> to ease pain or headaches can all help. In some cases, antiviral medication may be prescribed.
        <br>Polio or severe cases of meningitis or encephalitis may require additional treatment, such as breathing assistance or IV fluids.
        <br>If an animal that’s suspected to have the rabies virus bites you, you’ll be given a series of shots to help prevent the rabies virus from infecting you.
        </p>
    
    <h3>Prevention</h3>
    <p>There’s a vaccine for both <a href="https://www.healthline.com/health/polio-vaccine-side-effects">poliovirus</a> and the <a href="https://www.healthline.com/health/mmr-vaccine"> mumps virus</a>, which can cause meningitis and encephalitis.
        <br>Practicing good hygiene, avoiding close contact with those who have the virus, and protecting against insect bites can all help to reduce the spread of encephalitis and meningitis.
        <br>To reduce the risk of spreading rabies, keep your pets vaccinated and avoid approaching wild animals.
        </p>

        <br><br><br>

    <h3 id="bottom">Bottom line</h3>
    <p>There are many viral diseases. Some, such as the common cold or the stomach flu, are minor and go away on their own within a few days. Others, however, are more serious.
        <br>Unlike bacterial infections, viral diseases don’t respond to antibiotics. Instead, treatment usually focuses on managing symptoms and supporting the immune system with plenty of rest and hydration.
        </p>
