    <h1 id="gastro">Gastrointestinal viral diseases</h1>
    <p>Gastrointestinal viral diseases affect your digestive tract. The viruses that cause them are contagious and usually lead to a condition called gastroenteritis, also called the stomach flu.
        <br>Common symptoms of gastrointestinal viral diseases include:</p>
        <ul>
            <li>abdominal cramps</li>
            <li>diarrhea</li>
            <li>vomiting</li>
        </ul>
    
    <h3>Examples</h3>
    <p>Examples of gastrointestinal viral diseases include:</p>
    <ul>
        <li><a href="https://www.healthline.com/health/norovirus">norovirus infection</a></li>
        <li><a href="https://www.healthline.com/health/rotavirus"> rotavirus infection</a></li>
        <li>some adenovirus infections</li>
        <li>astrovirus infection</li>
    </ul>
    <h3>Transmission</h3>
    <p>Gastrointestinal viruses are shed in the stool during bowel movements. Food or water that’s been contaminated by feces can spread the virus to others. You can also get the virus from sharing utensils or personal objects with someone who has a virus.</p>

    <h3>Prevention</h3>
    <p>You can prevent gastrointestinal viral diseases by washing your hands often, especially after using the bathroom. Wiping down contaminated surfaces and not sharing personal items or eating utensils can also help.
        There’s also a vaccine for rotavirus that’s recommended as part of a child’s <a href="https://www.healthline.com/health/vaccinations/infant-immunization-schedule">vaccination schedule</a>.</p>

