    <h1 id="hemor">Hemorrhagic viral diseases</h1>
    <p>Hemorrhagic viral diseases are severe conditions that involve damage to your circulatory system.
        <br>Symptoms of a hemorrhagic viral disease include:</p>
        <ul>
            <li>high fever</li>
            <li>body aches</li>
            <li>weakness</li>
            <li>bleeding under the skin</li>
            <li>bleeding from the mouth or ears</li>
            <li>bleeding in internal organs</li>
        </ul>

    <h3>Examples</h3>
    <p>Examples of viral hemorrhagic diseases include:</p>
        <ul>
            <li><a href="https://www.healthline.com/health/ebola-hemorrhagic-fever">Ebola</a> </li>
            <li>Lassa fever</li>
            <li><a href="https://www.healthline.com/health/dengue-fever">dengue fever</a> </li>
            <li><a href="https://www.healthline.com/health/yellow-fever">yellow fever</a> </li>
            <li>Marburg hemorrhagic fever</li>
            <li>Crimean-Congo hemorrhagic fever</li>
        </ul>

    <h3>Transmission</h3>
    <p>Some hemorrhagic viral diseases, such as dengue fever and yellow fever, are spread through the bite of an infected insect.
        <br>Others, such as Ebola, are spread to other people through contact with the blood or other bodily fluid of someone with the virus. Lassa fever is spread through inhaling or consuming the dried feces or urine of a rodent with the virus.</p>

    <h3>Treatment</h3>
    <p>There’s no specific treatment for hemorrhagic viral diseases.
        <br>It’s important to stay hydrated if you have a viral hemorrhagic disease. Some people may need intravenous (IV) fluids to maintain electrolyte balance. Supportive care to maintain hydration and electrolyte balance is essential. In some cases, the antiviral drug ribavirin may be given.</p>

    <h3>Prevention</h3>
    <p>Researchers are in the process of developing vaccines for several hemorrhagic viruses. A yellow fever vaccine is currently available for people traveling to areas where yellow fever is common.
        <br>If you live or work in an area where viral hemorrhagic diseases are common, you can do the following to reduce your risk:
        <br>Use proper protection, such as gloves, glasses, or a face shield, when working around people who have a virus.
        <br>Avoid being bitten by insects, especially mosquitos and ticks, by wearing protective clothing or using insect repellent.
        <br> Protect against rodent infestation by keeping food covered, removing garbage often, and making sure windows and doors are secured properly.
        </p>

