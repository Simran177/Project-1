    <h1 id="hepatic">Hepatic viral diseases</h1>
    <p>The hepatic viral diseases cause inflammation of the liver, known as viral hepatitis. The most commonTrusted Source types of viral hepatitis are hepatitis A, B, and C.
        <br>It is worth noting that diseases caused by other viruses, such as cytomegalovirus and the yellow fever virus, can also affect the liver.</p>
    
    <h3>Examples</h3>
    <p>Examples of hepatic viral diseases include:</p>
    <ul>
        <li><a href="https://www.healthline.com/health/hepatitis-a">hepatitis A</a> </li>
        <li><a href="https://www.healthline.com/health/hepatitis-b">hepatitis B</a> </li>
        <li><a href="https://www.healthline.com/health/hepatitis-c">hepatitis C</a> </li>
        <li><a href="https://www.healthline.com/health/delta-agent-hepatitis-d">hepatitis D</a> </li>
        <li><a href="https://www.healthline.com/health/hepatitis-e"> hepatitis E</a></li>
        
    </ul>

    <h3 id="heptrans">Transmission</h3>
    <p>Hepatitis B and C can be transmitted from person to person through bodily fluids. Sharing items that come in to contact with blood, such as needles or razors, can also spread the virus. Hepatitis B can be spread through sexual contact.
        <br>People get hepatitis A and E by consuming food or water that’s been contaminated with feces from someone with the virus.
        <br>You can only develop hepatitis D if you already have the hepatitis B virus.</p>
    
    <h3>Treatment</h3>
    <p>Treatments for hepatitis B, C, and D focus on managing symptoms. In some cases, a doctor might prescribe medication, such as antiviral drugs.
        <br>Treatment of hepatitis A and E involves supportive measures, such as getting plenty of rest, drinking fluids, and avoiding alcohol.</p>

    <h3>Prevention</h3>
    <p>There are vaccines for both hepatitis A and hepatitis B. There’s also a vaccine for hepatitis E, but it’s not available in the U.S.
        <br>Other ways to prevent viral hepatitis include not sharing needles or razors, practicing safe sex, and avoiding food or drinks that may be contaminated by feces.</p>

