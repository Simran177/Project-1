    <h1 id="cutan">Cutaneous viral diseases</h1>
    <p>Cutaneous viral diseases cause lesions or <a href="https://www.healthline.com/health/papule">papules</a> to form on the skin. In many cases, these lesions can stick around for a long time or come back after disappearing for a while.
    </p>

    <h3>Examples</h3>
    <p>Examples of cutaneous viral diseases include:</p>
    <ul>
        <li><a href="https://www.healthline.com/health/skin/warts"> warts</a>, including <a href="https://www.healthline.com/health/std/genital-warts"> genital warts</a></li>
        <li>oral herpes</li>
        <li><a href="https://www.healthline.com/health/std/genital-herpes"> genital herpes</a></li>
        <li><a href="https://www.healthline.com/health/molluscum-contagiosum"> molluscum contagiosum</a></li>
    </ul>

    <h3 id="cutantrans">Transmission</h3>
    <p>These viruses are contagious. They’re usually spread through close physical contact with someone who has the virus or touching a contaminated object, such as a towel or faucet handle.
    </p>

    <h3>Treatment</h3>
    <p>Papules that form due to warts or molluscum contagiosum often go away on their own. They can also be removed by simple in-office procedures, such as <a href="https://www.healthline.com/health/skin-disorders/freezing-warts">cryotherapy.</a> 
        <br>There’s no cure for herpes, but antiviral medications, such as acyclovir, can help to shorten or prevent outbreaks.</p>

    <h3>Prevention</h3>
    <p>Practicing good hygiene habits, avoiding the sharing of personal items, and avoiding close contact with people who have active lesions can reduce your risk of developing a cutaneous viral disease.
    </p>
