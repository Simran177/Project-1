    <p>Not all viral diseases are contagious. This means they aren’t always spread from person to person. But many of them are. Common examples of contagious viral diseases include the flu, the common cold, HIV, and herpes.
        <br>Other types of viral diseases spread through other means, such as the bite of an infected insect.</p>

